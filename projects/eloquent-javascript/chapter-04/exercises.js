// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(numbers) {
  if(numbers.length === 0){
    return 0;
  }
  // create a result set to index 0 of numbers
  let result = numbers[0];
  // create a for loop starting at 1st index of numbers
  for(let i = 1; i < numbers.length; i++){
    // add current number to result
    result += numbers[i];
  }

  // return result
  return result;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  // create new array
  let reversed = [];
  // loop through array and unshift elements to new array
  for(let element of array){
    reversed.unshift(element);
  }
  // return new array
  return reversed;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  // create new array
  let reversed = [];
  // loop through array and unshift elements to new array
  for(let element of array){
    reversed.unshift(element);
  }
  // loop through array and push element of reversed into same index in array
  for(let i = 0; i < array.length; i++){
    array[i] = reversed[i];
  }
  return array;
}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
  let rest = null;
  for(let i = array.length - 1; i >= 0; i--){
    rest = {
      value: array[i],
      rest: rest
    };
  }

  return rest;
}

